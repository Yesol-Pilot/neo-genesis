"""
Blog thumbnail generator — fills in /assets/blog/<slug>.png for any
BLOG_POSTS entry that does not yet have a per-post hero image.

Why this exists:
  /blog index renders <img src=/assets/blog/<slug>.png>. When the file is
  missing, browsers show a broken image icon. Up to P14, only the original
  10 hand-shipped posts had thumbnails; everything generated by the
  Blog Auto-Gen pipeline since 2026-04-27 had a 404 on the thumbnail.

Strategy:
  1. Read BLOG_POSTS from src/landing/src/lib/data/sbus.ts (single SSOT)
  2. For every entry that does NOT end in -ko (Korean translations share
     their EN parent's image via the slug.replace(/-ko$/, "") lookup in
     /blog/page.tsx) and does NOT have a corresponding .png in
     public/assets/blog/, generate one
  3. Use HF Inference Providers to call FLUX.1-dev, with the same provider
     rotation as scripts/hf_publish/generate_p9_og_images.py
     (wavespeed -> fal-ai -> replicate -> together -> nebius)
  4. Letterbox 1024x1024 -> 1200x630 cinematic
  5. Save PNG + WebP

Per-post prompts:
  Each prompt is themed off the post category and core concept. We avoid
  literal text rendering (FLUX struggles with glyphs) and keep a consistent
  Neo Genesis brand palette: dark navy void, cyan + accent color, editorial
  tech-magazine cover style.

Run:
    PYTHONIOENCODING=utf-8 python scripts/landing/generate_blog_thumbnails.py

Env required:
    HF_TOKEN   (in .env.local)

Idempotent: skips slugs that already have a .png. Safe to run after every
new pipeline run.
"""

